async def auto_filter(client, msg, spoll=False):
    message = msg
    if message.text.startswith("/"): return  # ignore commands
    if re.findall("((^\/|^,|^!|^\.|^[\U0001F600-\U000E007F]).*)", message.text):
        return
    if 2 < len(message.text) < 100:
        search = message.text
        files, offset, total_results = await get_search_results(search.lower(), offset=0, filter=True)
        if not files:
            # Static "Not Found" reply with channel join button
            btn = [[
                InlineKeyboardButton("⭕ Join Here ⭕", url=SPELL_LNK)
            ]]
            n = await message.reply_text(
                text=script.NOT_FILE_TXT.format(search),
                reply_markup=InlineKeyboardMarkup(btn)
            )
            await asyncio.sleep(30)
            await n.delete()
            try:
                await message.delete()
            except:
                pass
            return
    else:
        return

    # Hardcoded: file_secure = True, so prefix is 'filep'
    pre = 'filep'
    # Hardcoded: single button layout
    btn = [
        [
            InlineKeyboardButton(
                text=f"📂[{get_size(file.file_size)}]👉{file.file_name}", callback_data=f'{pre}#{file.file_id}'
            ),
        ]
        for file in files 
    ]

    if offset != "":
        key = f"{message.chat.id}-{message.id}"
        BUTTONS[key] = search
        req = message.from_user.id if message.from_user else 0
        btn.append(
            [InlineKeyboardButton(text=f"🗓 1/{math.ceil(int(total_results) / 10)}", callback_data="pages"),
             InlineKeyboardButton(text="NEXT ⏩", callback_data=f"next_{req}_{key}_{offset}")]
        )
    else:
        btn.append(
            [InlineKeyboardButton(text="🗓 1/1", callback_data="pages")]
        )
    # No IMDB — simple text caption
    cap = f"Hey 👋 Buddy 😎 \n \n Here Is The Results For #{search}"
    dll = await message.reply_text(cap, reply_markup=InlineKeyboardMarkup(btn))
    await asyncio.sleep(60)
    fll = await dll.edit_text(f"<b>🗑️ Filter Deleted After 1 Min ‼️ \n 🔍Search Again !!</b>")
    await asyncio.sleep(60)
    await fll.delete()
    try:
        await message.delete()
    except:
        pass


async def manual_filters(client, message, text=False):
    group_id = message.chat.id
    name = text or message.text
    reply_id = message.reply_to_message.id if message.reply_to_message else message.id
    keywords = await get_filters(group_id)
    for keyword in reversed(sorted(keywords, key=len)):
        pattern = r"( |^|[^\w])" + re.escape(keyword) + r"( |$|[^\w])"
        if re.search(pattern, name, flags=re.IGNORECASE):
            reply_text, btn, alert, fileid = await find_filter(group_id, keyword)

            if reply_text:
                reply_text = reply_text.replace("\\n", "\n").replace("\\t", "\t")

            if btn is not None:
                try:
                    if fileid == "None":
                        if btn == "[]":
                            dm =await client.send_message(
                                group_id, 
                                reply_text, 
                                disable_web_page_preview=True,
                                reply_to_message_id=reply_id)
                            await asyncio.sleep(30)

                            await dm.delete()

                            await message.delete()
                        else:
                            button = eval(btn)
                            dm= await client.send_message(
                                group_id,
                                reply_text,
                                disable_web_page_preview=True,
                                reply_markup=InlineKeyboardMarkup(button),
                                reply_to_message_id=reply_id
                            )
                            await asyncio.sleep(30)
                            await dm.delete()
                            await message.delete()
                    elif btn == "[]":
                        dm= await client.send_cached_media(
                            group_id,
                            fileid,
                            caption=reply_text or "",
                            reply_to_message_id=reply_id
                        )
                        await asyncio.sleep(30)
                        await dm.delete()
                        await message.delete()
                    else:
                        button = eval(btn)
                        dm= await message.reply_cached_media(
                            fileid,
                            caption=reply_text or "",
                            reply_markup=InlineKeyboardMarkup(button),
                            reply_to_message_id=reply_id
                        )
                        await asyncio.sleep(30)
                        await dm.delete()
                        await message.delete()
                except Exception as e:
                    logger.exception(e)
                break
    else:
        return False
